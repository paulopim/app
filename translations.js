function normalizeString(str) {
    return str.replace(/\s+/g, ' ').trim();
}

function traverseAndTranslate(node) {
    if (node.nodeType === Node.TEXT_NODE) {
        let text = normalizeString(node.nodeValue);
        
        // Sometimes strings contain emojis like 🔹 or 🧽 or ✨ or 👉, we need to check if dict contains the text without emojis maybe, 
        // but here we just check direct match. We can also do a loop if parts match.
        if (text && es_en_dict[text]) {
            node.nodeValue = node.nodeValue.replace(node.nodeValue.trim(), es_en_dict[text]);
        } else if (text) {
            // handle prefixed emojis like "✨ Resultado: tu coche..." -> if matching part
            for (const [key, val] of Object.entries(es_en_dict)) {
                if (text === key || text.includes(key)) {
                     // Only replace exact matches or substantial parts to avoid replacing short words accidentally
                     if (key.length > 10) {
                         node.nodeValue = node.nodeValue.replace(key, val);
                     }
                }
            }
        }
        
    } else if (node.nodeType === Node.ELEMENT_NODE) {
        if (node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE' && !node.classList.contains('no-translate')) {
            if (node.placeholder) {
                let text = normalizeString(node.placeholder);
                if (text && es_en_dict[text]) node.placeholder = es_en_dict[text];
            }
            node.childNodes.forEach(traverseAndTranslate);
        }
    }
}

function applyTranslation(lang) {
    if (lang === 'en') {
        traverseAndTranslate(document.body);
        document.documentElement.lang = 'en';
        let btnText = document.getElementById('lang-btn-text');
        if (btnText) btnText.textContent = 'Español';
        let btnImg = document.getElementById('lang-btn-img');
        if (btnImg) btnImg.src = 'https://flagcdn.com/w40/es.png';
    } else {
        // Switching back to ES: reload the page to restore real DOM
        let currentLang = localStorage.getItem('site_lang');
        if (currentLang === 'en') {
            localStorage.setItem('site_lang', 'es');
            window.location.reload();
        }
    }
}

function toggleLanguage() {
    let current = localStorage.getItem('site_lang') || 'es';
    let next = current === 'es' ? 'en' : 'es';
    localStorage.setItem('site_lang', next);
    if (next === 'en') {
        applyTranslation('en');
    } else {
        window.location.reload();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    let current = localStorage.getItem('site_lang') || 'es';
    if (current === 'en') {
        applyTranslation('en');
    }
});
